class Place {
  final int id;
  final String name;
  final String description;
  final String image;
  final double lat;
  final double lng;
  final String policePhone;
  final String fireServicePhone;

  Place({
    required this.id,
    required this.name,
    required this.description,
    required this.image,
    required this.lat,
    required this.lng,
    required this.policePhone,
    required this.fireServicePhone,
  });
}
